    public function searchbyName($searchVal=null)
    {
        $em = $this->getEntityManager();
        $queryBuilder = $em->createQueryBuilder();
        if($searchVal!=null){
        return $queryBuilder->select('q') 
            ->from(Quiz::class, 'q')
            ->where($queryBuilder->expr()->like('q.titre', ':searchVar'))
            ->setParameter('searchVar', '%'.$searchVal.'%')
            ->getQuery()
            ->getResult();
        }
        return $queryBuilder->select('q')
        ->from(Quiz::class, 'q')
        ->orderBy('q.titre','Asc')
        ->getQuery()
        ->getResult();
    }

